/**
 * abstract object that has x and y properties
 */
export interface XY {
    x: number;
    y: number;
}

/**
 * abstract object that has x, y, and z properties
 */
export interface XYZ {
    x: number;
    y: number;
    z: number;
}

/**
 * abstract object that has x, y, z, and w properties
 */
export interface XYZW {
    x: number;
    y: number;
    z: number;
    w: number;
}
